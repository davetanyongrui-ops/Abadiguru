function changeLanguage(lang) {
  if (!translations[lang]) return;
  const t = translations[lang];

  // Update html lang attribute
  document.documentElement.lang = lang;

  // Update page title
  const titleKey = document.documentElement.dataset.pageTitle;
  if (titleKey && t[titleKey]) document.title = t[titleKey];

  // Translate text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // Translate innerHTML (for elements with <strong>, <em>, etc.)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Translate placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Update active lang button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const active = btn.dataset.lang === lang;
    btn.classList.toggle('font-bold', active);
    btn.classList.toggle('text-[#4a7c59]', active);
    btn.classList.toggle('text-slate-500', !active);
    btn.classList.toggle('font-normal', !active);
  });

  localStorage.setItem('website_lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
  changeLanguage(localStorage.getItem('website_lang') || 'id');
});
